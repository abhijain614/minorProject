    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        if(ValidateInputs()){
            User user= new User();
            user.setEmail(email);
            user.setPassword(encPassword);
            user.setMobileNumber(mobileNumber);
            try {
                userID = UsersDAO.verifyUser(user);
                //System.out.println(userID);
                userType = UsersDAO.getUsertypeByUserID(userID);
            } catch (SQLException ex) {
                System.out.println("SQL Exception while logging in :");
                ex.printStackTrace();
            }
            if(userID!=0){
                System.out.println("Successfully Logged in....");
                UserProfile.setID(userID);
                UserProfile.setEmailID(email);
                UserProfile.setPassword(encPassword);
                UserProfile.setUserType(userType);
                DashboardFrame dashboardFrame = new DashboardFrame(user);
                this.setVisible(false);
                dashboardFrame.setVisible(true);
            }
            else{
                System.out.println("USER NOT FOUND !");
            }
        }
        else{
        JOptionPane.showMessageDialog(null,"Invalid Credentials!" , "Error!", JOptionPane.ERROR_MESSAGE);    
        }
    }//GEN-LAST:event_jLabel6MouseClicked

