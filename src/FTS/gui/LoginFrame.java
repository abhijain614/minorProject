    private boolean ValidateInputs() {
        boolean isAllRight=false;
        String text = textField.getText();
        char[] pwd = password.getPassword();
        String userPassword = String.valueOf(pwd);
        if(text.length()==0||userPassword.length()==0){
        return false;
        }
        if((text.matches(EMAIL_REGEX))){
        isAllRight=true;
        email = text;
        }
        if(text.matches(PHONE_REGEX)){
        isAllRight=true;
        mobileNumber = Long.parseLong(text);
        }
        encPassword = ServerConfig.encrypt(userPassword);
        return isAllRight;
    }
